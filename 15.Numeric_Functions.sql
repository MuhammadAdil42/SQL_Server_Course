SELECT ABS(-5);     /* returns Absolute value of a number */

SELECT CEILING(76.35);  /* returns Upper digit of a decimal number */

SELECT FLOOR(78.9);    /* returns lower digit of a decimal number */

SELECT SIGN(-9), SIGN(10), SIGN(0);  /* SIGN function returns -1 when parameter is negative, returns 1 when parameter is positive and returns 0 when parameter is 0 */

SELECT SQUARE(3), SQRT (64), PI(), COS(20), SIN(30), TAN(50);

SELECT EXP(2);  /* returns exponential of a number */